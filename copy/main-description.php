

<h3 class="ui header">What the heck is this?</h3>

<p>
Carrot/Stick is a conference for the behavioral economics enthusiast &mdash; 
the person who uses behavioral economics in their everyday life. 
We're the kind of people who hear about New Year's Resolutions and think 
"I could do that &mdash; but how can I take it one step further?" 
Where some people rely on willpower, we use creative motivation devices. 
Carrot/Stick is a place to talk about the strategies &mdash; 
from the ridiculous to sublime &mdash; 
that you use in your own life to reach your goals. 
You'll hear about trackers and apps, yes, but you'll also hear about 
<span class="popup" data-title="Akrasia" 
      data-content="(/əˈkrāZH(ē)ə/) the state of mind in which someone acts against their better judgment through weakness of will" 
      data-variation="inverted">Akrasia</span> 
and playing 
<a href="http://blog.beeminder.com/pomopoker/">Pomodoro Poker</a>.
</p>

<p>
We love this stuff, and there was nowhere in meatspace to talk about it. 
Some conferences are too academic for the hobbyist, others are focused on 
tracking your life's data, but not using that data to then change your life. 
We envision a place where you won't be met with uncertain looks when you talk 
about paying $5000 to the presidential candidate you hate if you don't finish 
your thesis on time. 
This is a safe space for those that want to take behavioral economics research 
into their own lives. 
You don't have to explain the reasoning why, you just get to start having the 
conversation with other people as excited about this stuff as you are.
</p>

